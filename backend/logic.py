def calculate_match(answers: dict):
    # 全部0点で初期化
    total_scores = {member: 0 for member in dramas}

    # 回答ごとに加算
    for q_id, answer in answers.items():
        if q_id in scoring and answer in scoring[q_id]:
            for member, points in scoring[q_id][answer].items():
                total_scores[member] += points

    # 一番高い作品を選ぶ
    result = max(total_scores, key=total_scores.get)

    return result, total_scores
